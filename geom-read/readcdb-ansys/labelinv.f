      subroutine labelinv(i,k1,k2)
      implicit none
      integer i,k1,k2
      if (i.eq.1) then
         k1=1
         k2=2
      else
      endif
      if (i.eq.2) then
         k1=1
         k2=3
      else
      endif
      if (i.eq.3) then
         k1=1
         k2=4
      else
      endif
      if (i.eq.4) then
         k1=2
         k2=3
      else
      endif
      if (i.eq.5) then
         k1=2
         k2=4
      else
      endif
      if (i.eq.6) then
         k1=3
         k2=4
      else
      endif

      return
      end
